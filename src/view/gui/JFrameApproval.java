    private void loadModel() {
        try {
            model.addColumn("Id");
            model.addColumn("Email");
            model.addColumn("Username");
            model.addColumn("Name");
            model.addColumn("Age");
            model.addColumn("Date");
            model.addColumn("ApprovalStatus");
            model.addColumn("MatriculationFee");

            ApprovalTable.setModel(model);

            loadFile();

        } catch (IOException e) {
            System.out.println(e);
        }
    }

    private void loadFile() throws IOException {
        String row[];
        try {
            FileReader file = new FileReader("StudentList.txt");
            BufferedReader reading = new BufferedReader(file);

            String line = reading.readLine();
            while (line != null) {
                row = line.split("%");

                // Verificar si el estado de aprobaciÃ³n es 2
                if (row.length > 6 != "2".equals(row[6])) {
                    model.addRow(row);
                }

                line = reading.readLine();
            }

        } catch (FileNotFoundException e) {
            System.out.println(e);
        }
    }

